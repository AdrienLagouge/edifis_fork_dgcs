# Appelée dans le pgm server.R, la fonction modif_df() permet de ne garder que les deux premières colonnes du fichier importé, de supprimer les lignes vides et enfin d'attribuer
# à chaque valeur un nom. Elle prend deux arguments : le dataframe à "nettoyer" et le vecteur de noms à lui associer.

modif_df <- function(dataframe, varnames) {
  if (ncol(dataframe) >= 2) {
    df <- dataframe[, 1:2]
    df <- df[rowSums(is.na(df[, 2])) == 0, ]
  }

  for (i in 1:length(varnames)) {
    df[i, 3] = varnames[i]
  }

  df[, c(1, 2, 3)] <- df[, c(1, 3, 2)]
  setnames <- setNames(df$...3, df$Valeur)
  return(setnames)
}
